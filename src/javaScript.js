function addCell (tr,val) {
  var td = document.createElement('td');
  td.innerHTML = val;

  tr.appendChild(td);
}

function addHeaders (tr, val){
  var td = document.createElement('th');
  td.innerHTML = val;

  tr.appendChild(td);
}

function addRowHeaders (tbl, val_1, val_2, val_3, val_4, val_5, val_6, val_7) {
  var tr = document.createElement('tr');

  addHeaders(tr,val_1);
  addHeaders(tr,val_2);
  addHeaders(tr,val_3);
  addHeaders(tr,val_4);
  addHeaders(tr,val_5);
  addHeaders(tr,val_6);
  addHeaders(tr,val_7);

  tbl.appendChild(tr);

}


function addRow (tbl, val_1, val_2, val_3, val_4, val_5, val_6, val_7) {
  var tr = document.createElement('tr');

  addCell(tr,val_1);
  addCell(tr,val_2);
  addCell(tr,val_3);
  addCell(tr,val_4);
  addCell(tr,val_5);
  addCell(tr,val_6);
  addCell(tr,val_7);

  tbl.appendChild(tr);

}

